package BRUNO_EDUARDO_CALLEGARO.prova;

public class Aluno {
    private String name;
    private String ra;

    public Aluno(String name, String ra) {
        this.name = name;
        this.ra = ra;
    }

    public String getName() {
        return name;
    }

    public String getRa() {
        return ra;
    }
}
